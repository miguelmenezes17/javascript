let vet = [7,9,6,4,2,8,1,5,3,5,2,0,4] // Determina um array
console.log(vet) // Nos mostra o array
vet.push(3) // Acrescenta o valor 3 na array
console.log(vet) // Nos mostra o array, com o 3 em sua posição
vet.sort() // Organiza o vetor em ordem crescente
console.log(`Nosso vetor é o ${vet}`) //Escreve o vetor no terminal
console.log(`O primeiro valor dentro de nosso vetor é o ${vet[0]}`) //Nos mostra o primeiro valor dentro do vetor
console.log(`Nós temos ${vet.length} valores dentro de nosso vetor ${vet}`) // Nos mostra quantos valores temos dentro do vetor
console.log(`O ultimo valor dentro de nosso vetor é o ${vet[13]}`) // Nos mostra qual é o ultimo valor dentro de nosso vetor